import { DeviceType, TicketCardType, TicketStatus } from "./definitions";

interface customer {
    id: string;
    name: string;
    phone: string;
    email: string;
}


interface ticket {
    id: string;
    customer_id: string; // link to customer's id
    device_type: DeviceType;
    device_brand: string;
    device_model: string;
    issue_description: string; // description of the issue
    technician_notes?: string; // notes from the technician
    etr?: Date;
    photo: string | null;
    timeline: {
        created_at: Date;
        updated_at: Date;
    };
    payment: {
        repair_cost: number;
        parts_cost: number;
        tax: number;
        total: number;
        paid: boolean;
    };
    status: TicketStatus;
    ticket_number: string; // this is generated by the system. It is used to identify device repair tickets.
}

